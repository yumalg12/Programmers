const readline = require('readline');
const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout
});

let input = [];

rl.on('line', function (line) {
    input = line.split(' ');
}).on('close', function () {
    console.log(Array(+input).fill(0).map((_,i)=>'*'.repeat(i+1)).join('\n'));
});